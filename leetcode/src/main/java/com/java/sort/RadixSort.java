public class RadixSort {

    /**
     * 基数排序
     *
     * 对数组里每个位（个位、十位等）分别进行排序，最后得出有序数组
     */
    public static int[] radixSort(int[] m) {
        int[] n = Arrays.copyOf(m, m.length);

        return n;
    }
}
